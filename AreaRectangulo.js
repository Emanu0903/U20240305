// Para esta funcion tomaremos dos parametros que seria el alto y el ancho deonde nos va a devolver le area calculada del rectangulo

function calcularAreaRectangulo(ancho,alto) {
    return ancho * alto;
}

//Aplicamos las medidas

let area = calcularAreaRectangulo(10,20);
console.log("El area del rectangulo es: ", area);

